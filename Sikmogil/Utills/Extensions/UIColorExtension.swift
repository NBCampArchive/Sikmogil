import UIKit

// UIColor Extension
// 사용 예시 ) view.backgroundColor = .customGreen
extension UIColor {
    static let customBlack = UIColor(named: "appBlack")
    static let customDarkGray = UIColor(named: "appDarkGray")
    static let customGreen = UIColor(named: "appGreen")
    static let customLightGray = UIColor(named: "appLightGray")
    static let customPurple = UIColor(named: "appPurple")
    static let customSkyBlue = UIColor(named: "appSkyBlue")
    static let customYellow = UIColor(named: "appYellow")
}
